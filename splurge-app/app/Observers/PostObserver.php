    /**
     * Handle the Post "deleted" event.
     *
     * @param  \App\Models\Post  $post
     * @return void
     */
    public function deleted(Post $post)
    {
        MediaOwner::where([
            'owner_type' => PostItem::class,
            'owner_id' => PostItem::select('id')->where('post_id', $post->id)])->delete();

        $post->items()->delete();
        $post->taggables()->delete();
    }

